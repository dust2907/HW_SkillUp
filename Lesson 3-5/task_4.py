# =======================================================================
# ===== Дан текстовый файл. Найти длину самой длинной строки ============
# =======================================================================

with open("text2.txt", "r", encoding="utf8") as file:
    text = file.readlines()
for i in text:
    max_l = 0
    if len(i) > max_l:
        max_l = len(i)
print(max_l)
